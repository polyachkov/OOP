package nsu.pizza.info;

public class CookerInfo {

    public int cookerNumber;
    public int timeToCook;

    public int getCookerNumber() {
        return cookerNumber;
    }

    public void setCookerNumber(int cookerNumber) {
        this.cookerNumber = cookerNumber;
    }

    public int getTimeToCook() {
        return timeToCook;
    }

    public void setTimeToCook(int timeToCook) {
        this.timeToCook = timeToCook;
    }
}
