package nsu.pizza.info;

public class DeliveryInfo {

    public int numberDeliver;
    public int timeToDeliver;
    public int numberOfPizzas;

    public int getNumberDeliver() {
        return numberDeliver;
    }

    public void setNumberDeliver(int numberDeliver) {
        this.numberDeliver = numberDeliver;
    }

    public int getTimeToDeliver() {
        return timeToDeliver;
    }

    public void setTimeToDeliver(int timeToDeliver) {
        this.timeToDeliver = timeToDeliver;
    }

    public int getNumberOfPizzas() {
        return numberOfPizzas;
    }

    public void setNumberOfPizzas(int numberOfPizzas) {
        this.numberOfPizzas = numberOfPizzas;
    }
}
